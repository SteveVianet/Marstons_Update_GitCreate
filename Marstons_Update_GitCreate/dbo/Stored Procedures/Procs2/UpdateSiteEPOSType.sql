CREATE PROCEDURE [dbo].[UpdateSiteEPOSType]
(
	@EDISID		INT,
	@EPOSType	INT,
	@UpdateID	ROWVERSION = NULL	OUTPUT
)

AS


IF 	(SELECT COUNT(*)
	FROM dbo.Sites 
	WHERE EDISID = @EDISID 
	AND UpdateID = @UpdateID) > 0 
     OR @UpdateID IS NULL
BEGIN
	UPDATE dbo.Sites
	SET EPOSType = @EPOSType
	WHERE EDISID = @EDISID

	SET @UpdateID = (SELECT UpdateID FROM Sites WHERE EDISID = @EDISID)

	RETURN 0

END
ELSE
BEGIN
	RETURN -1

END

GO
GRANT EXECUTE
    ON OBJECT::[dbo].[UpdateSiteEPOSType] TO PUBLIC
    AS [dbo];

